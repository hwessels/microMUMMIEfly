#---------------------------------------------------------------------
#                           PUBLIC METHODS
#---------------------------------------------------------------------
sub generate
{
  while(1)
    {
      my $n=int(rand(1000000)+100000);
      my $filename="$n.tmp";
      return($filename) unless -e $filename;
    }
}
#---------------------------------------------------------------------






#---------------------------------------------------------------------
#                         PRIVATE METHODS
#---------------------------------------------------------------------

1;

